package com.apnacollege;

public class MathClass {

    public static void main (String[] args)
    {
        //Maths
        //5,6 (print max)

        System.out.println(Math.max(5,6));
        System.out.println(Math.min(5,6));
        System.out.println(Math.random()); // print any random in long format and
        // if we want to show the output in Int format, then we can typecast it to int.
        System.out.println((int)Math.random()); // Random function gives the value between 0.1 to 1.0 value.
        System.out.println((int)(Math.random()*100));



    }
}
