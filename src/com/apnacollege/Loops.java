package com.apnacollege;

public class Loops {
    public static void main(String[] args) {

       /* //Traditional
        System.out.println("1");
        System.out.println("2");
        System.out.println("3");

        */

        // 1- 100 Loops
        // 3 types of loops

       /*  for loops
                do while loops
                while loops
                */



        for(int i = 0;i<=100;i++) // Ascending order
            // initialize , upto this, update
        {
            System.out.println(i);
        }

        // 100-1 loops ( for reserve)
        for (int j=100; j>=1;j--)
        {
            System.out.println(j);
        }









    }
}
